<p style="margin-left:.25in; margin-right:0in"><strong><u><span style="color:#333333">About International School Award</span></u></strong></p>

<p style="margin-left:.25in; margin-right:0in"><span style="color:#333333">International School Award (ISA) by the British Council is a benchmarking system that accredits schools having an outstanding level of support for nurturing global citizenship in young people and enriching teaching and learning. It encourages schools to collaborate with overseas schools to create a great learning experience for students through various pedagogical practices. It offers the International School Award (ISA) as an accreditation framework for schools to record and evaluate their international work and embed it into the curriculum and school ethos.</span></p>

